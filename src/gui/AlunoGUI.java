    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (jTextFieldCPF.getText().isEmpty() || jTextFieldNome.getText().isEmpty() || jTextFieldData.getText().isEmpty() || jTextFieldPeso.getText().isEmpty() || jTextFieldAltura.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, preencha todos os campos!", "Aviso", JOptionPane.WARNING_MESSAGE);
        }else {
            Aluno aluno = new Aluno(Integer.parseInt(jTextFieldCPF.getText()), String.valueOf(jTextFieldNome.getText()), LocalDate.parse(jTextFieldData.getText(), DateTimeFormatter.ofPattern("dd-MM-yyyy")), Float.parseFloat(jTextFieldPeso.getText()), Float.parseFloat(jTextFieldAltura.getText()));
            AlunoDAO dao = new AlunoDAO();
            try {
                if (dao.cpfJaCadastrado(jTextFieldCPF.getText())) {
                    JOptionPane.showMessageDialog(null, "Erro CPF já cadastrado no sistema", "Erro", JOptionPane.ERROR_MESSAGE);
                } else {
                    dao.inserir(aluno);
                    JOptionPane.showMessageDialog(null, "Aluno " + jTextFieldNome.getText() + " inserido com sucesso", "Informação", JOptionPane.INFORMATION_MESSAGE);
                    jTextFieldAltura.setText("");
                    jTextFieldCPF.setText("");
                    jTextFieldData.setText("");
                    jTextFieldNome.setText("");
                    jTextFieldPeso.setText("");
                }
            }catch (SQLException ex) {
                Logger.getLogger(AlunoGUI.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Erro ao inserir aluno: " + ex.getMessage());
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

